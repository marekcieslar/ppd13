function monthName(num) {
    switch (num) {
        case 0:
            return 'styczeń';
        case 1:
            return 'luty';
        case 2:
            return 'marzec';
        case 3:
            return 'kwiecień';
        case 4:
            return 'maj';
        case 5:
            return 'czerwiec';
        case 6:
            return 'lipiec';
        case 7:
            return 'sierpień';
        case 8:
            return 'wrzesień';
        case 9:
            return 'październik';
        case 10:
            return 'listopad';
        case 11:
            return 'grudzień';
    }
}
